_ROOT = pathlib.Path(__file__).parent.parent  # project root
_ENV_FILE = _ROOT / ".env"

app = FastAPI(docs_url=None, redoc_url=None)

_shutdown_event = threading.Event()


# ── helpers ────────────────────────────────────────────────────────────────────

def _store_secret(key: str, value: str) -> None:
    try:
        from argus.secrets import set_secret
        set_secret(key, value)
    except Exception:
        pass


def _write_env(fields: dict[str, str]) -> None:
    lines = [
        "# Argus configuration — generated by setup wizard\n",
        "# Secrets (API keys, passwords) are stored in the OS keychain.\n\n",
    ]
    for k, v in fields.items():
        lines.append(f"{k}={v}\n")
    _ENV_FILE.write_text("".join(lines))
    _ENV_FILE.chmod(0o600)


# ── routes ─────────────────────────────────────────────────────────────────────

@app.get("/", response_class=HTMLResponse)
async def wizard_page() -> HTMLResponse:
    return HTMLResponse(_WIZARD_HTML)


@app.post("/api/setup")
async def do_setup(request: Request) -> JSONResponse:
    data: dict[str, Any] = await request.json()
    errors: list[str] = []

    # Required fields
    if not data.get("robinhood_username"):
        errors.append("Robinhood email is required.")
    if not data.get("robinhood_password"):
        errors.append("Robinhood password is required.")
    if not data.get("anthropic_api_key"):
        errors.append("Anthropic API key is required.")
    if errors:
        return JSONResponse({"ok": False, "errors": errors})

    # Store secrets in keychain
    _store_secret("ROBINHOOD_PASSWORD", data["robinhood_password"])
    _store_secret("ANTHROPIC_API_KEY", data["anthropic_api_key"])
    if data.get("gemini_api_key"):
        _store_secret("GEMINI_API_KEY", data["gemini_api_key"])
    if data.get("robinhood_mfa_secret"):
        _store_secret("ROBINHOOD_MFA_SECRET", data["robinhood_mfa_secret"])
    if data.get("ntfy_url"):
        pass  # ntfy is not a secret, goes in .env

    # Build .env fields
    watchlist = data.get("watchlist", "AAPL,TSLA,NVDA,BTC,ETH").strip().upper()
    env_fields = {
        "ROBINHOOD_USERNAME": data["robinhood_username"].strip(),
        "AGENTIC_ACCOUNT_NUMBER": data.get("agentic_account", "").strip(),
        "DEFAULT_ACCOUNT_NUMBER": data.get("default_account", "").strip(),
        "PAPER_TRADE": "true" if data.get("paper_trade", True) else "false",
        "APPROVAL_THRESHOLD": data.get("approval_threshold", "medium"),
        "WATCHLIST": watchlist,
        "MAX_POSITION_PCT": str(round(float(data.get("max_position_pct", 0.10)), 2)),
        "STOP_LOSS_PCT": str(round(float(data.get("stop_loss_pct", 0.05)), 2)),
        "MAX_POSITIONS": str(int(data.get("max_positions", 5))),
        "DAILY_DRAWDOWN_LIMIT": str(round(float(data.get("daily_drawdown_limit", -0.05)), 2)),
        "MIN_CONFIDENCE": str(round(float(data.get("min_confidence", 0.65)), 2)),
        "MAX_POSITION_LOSS_USD": str(round(float(data.get("max_position_loss_usd", 75.0)), 2)),
        "MONTHLY_API_BUDGET": str(round(float(data.get("monthly_api_budget", 20.0)), 2)),
        "INTERVAL_OPEN": "90",
        "INTERVAL_PREMARKET": "180",
        "INTERVAL_AFTERHOURS": "180",
        "INTERVAL_CLOSED": "300",
        "WEB_HOST": "127.0.0.1",
        "WEB_PORT": "8000",
    }
    if data.get("ntfy_url"):
        env_fields["NTFY_URL"] = data["ntfy_url"].strip()

    _write_env(env_fields)

    # Shut down wizard after a short delay so the response reaches the browser
    def _stop():
        import time; time.sleep(1.5)
        _shutdown_event.set()
    threading.Thread(target=_stop, daemon=True).start()

    return JSONResponse({"ok": True})


@app.get("/api/prefill")
async def prefill() -> JSONResponse:
    """Return any existing .env values so the form can pre-populate on re-run."""
    if not _ENV_FILE.exists():
        return JSONResponse({})
    result: dict[str, str] = {}
    for line in _ENV_FILE.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            k, _, v = line.partition("=")
            result[k.strip()] = v.strip()
    return JSONResponse(result)


