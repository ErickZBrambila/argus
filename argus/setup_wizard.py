"""Browser-based first-run setup wizard.

Run with:  argus-setup-gui
Opens http://localhost:8888 and walks through credentials → accounts →
watchlist → risk settings, then writes .env and stores secrets in the
OS keychain.  Shuts down automatically when setup completes.
"""

